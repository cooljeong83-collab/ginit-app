function metroShortToken(sido) {
  return sido.replace(/특별시|광역시|특별자치시|특별자치도/g, '').trim();
}

function buildEntries() {
  const entries = [];
  for (const [sido, gus] of Object.entries(METRO_SIGUNGU)) {
    const token = metroShortToken(sido);
    for (const gu of gus) {
      entries.push({ key: `${token} ${gu}`, label: `${sido} ${gu}`, cityToken: token });
    }
  }
  for (const row of SI_SIGUNGU_GUS) {
    const cityShort = row.city.replace(/시$/, '');
    for (const gu of row.gus) {
      entries.push({ key: `${cityShort} ${gu}`, label: `${row.sido} ${row.city} ${gu}`, cityToken: cityShort });
    }
  }
  return entries;
}

function escapeTs(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));
const entries = buildEntries();
const centers = {};

for (const e of entries) {
  const q = encodeURIComponent(`${e.label} 대한민국`);
  const url = `https://nominatim.openstreetmap.org/search?q=${q}&format=json&limit=1`;
  try {
    const res = await fetch(url, { headers: { 'User-Agent': 'ginit-app-feed-region-centers/1.0' } });
    const data = await res.json();
    if (data[0]) {
      centers[e.key] = { latitude: Number(data[0].lat), longitude: Number(data[0].lon) };
      process.stderr.write('.');
    } else {
      process.stderr.write('x');
    }
  } catch {
    process.stderr.write('!');
  }
  await sleep(1100);
}
process.stderr.write('\n');

for (const e of entries) {
  if (centers[e.key]) continue;
  const fb = CITY_FALLBACK[e.cityToken];
  if (fb) centers[e.key] = { ...fb };
}

const body = Object.entries(centers)
  .sort(([a], [b]) => a.localeCompare(b, 'ko'))
  .map(([k, v]) => `  '${escapeTs(k)}': { latitude: ${v.latitude}, longitude: ${v.longitude} },`)
  .join('\n');

const ts = `/** @generated by scripts/generate-feed-interest-region-sync-centers.mjs — do not edit manually */
import type { LatLng } from '@/src/lib/geo-distance';

export const FEED_NON_SEOUL_GU_SYNC_CENTERS: Readonly<Record<string, LatLng>> = {
${body}
};
`;

writeFileSync(OUT, ts, 'utf8');
console.log(`Wrote ${Object.keys(centers).length} centers → ${OUT}`);
