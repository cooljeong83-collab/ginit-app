export function pointInSeoulGuLatLngBounds(
  lat: number,
  lng: number,
  gu: SeoulGuLabel,
): boolean {
  if (!Number.isFinite(lat) || !Number.isFinite(lng)) return false;
  const b = SEOUL_GU_LATLNG_BOUNDS[gu];
  return lat >= b.latMin && lat <= b.latMax && lng >= b.lngMin && lng <= b.lngMax;
}
